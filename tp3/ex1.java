package tp3;
public class ex1 {
    public static void main(String[] args) {
        // Déclarer et initialiser un tableau 1D
        int[] nombres = {10, 20, 30, 40, 50}; // Exemple de tableau d'entiers

        // Afficher les éléments du tableau 1D
        System.out.println("Les éléments du tableau sont :");
        for (int i = 0; i < nombres.length; i++) {
            System.out.println("Élément " + i + ": " + nombres[i]);
        }
    }
}

